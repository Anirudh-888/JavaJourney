public class Ternary_operator {
    public static void main(String[] args){
        int age = 17;
        //this is the usage of ternary operator
        //Ternary operator is the short form of if and else statements
        String vote = (age>=18)?"He can vote":"He cannot vote";
        //Printing out what the value of the String stores
        System.out.println(vote);
    }
    
}
